function normalizeName(value: string) {
  return value.trim().toLocaleUpperCase("id-ID");
}

function createSummary(item: Umkm) {
  const content = (item.description ?? item.product_description ?? "Usaha lokal warga Dusun Karangtalun.")
    .replace(/\s+/g, " ")
    .trim();

  return content.length > 220 ? `${content.slice(0, 217).trimEnd()}…` : content;
}

export function getPetaUmkmLocations(items: Umkm[]): PetaUmkmLocation[] {
  const itemsByName = new Map(items.map((item) => [normalizeName(item.name), item]));

  return staticUmkmLocations.flatMap((location) => {
    const item = itemsByName.get(normalizeName(location.name));

    if (!item) return [];

    return [{
      id: item.id,
      name: item.name,
      slug: item.slug,
      category: item.umkm_categories?.name ?? "UMKM Karangtalun",
      summary: createSummary(item),
      address: item.address,
      whatsapp: item.whatsapp,
      mapUrl: item.map_url,
      imageUrl: item.image_url,
      coordinates: location.coordinates,
    }];
  });
}

